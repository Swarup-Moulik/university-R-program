my_words <- c("apple", "cat", "Ball", "great", "elder")
print("Original character vector:")
print(my_words)

# Convert all words to uppercase
uppercase_words <- toupper(my_words)
print("\nVector with all words in uppercase:")
print(uppercase_words)

# Sort the words alphabetically
sorted_words <- sort(uppercase_words)
print("\nFinal sorted vector:")
print(sorted_words)